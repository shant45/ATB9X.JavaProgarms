package Oct.ex_016102024;

public class Lab018_decrement {
    public static void main(String[] args) {
        int a4 = 5 ;
        int b4 = a4--;

        System.out.println(a4);
        System.out.println(b4);

        int a5 = 5;
        int b5 = --a5 ;

        System.out.println(a5);
        System.out.println(b5);
    }
}
